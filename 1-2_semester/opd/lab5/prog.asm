ORG 0x139
TEMP: WORD 0x0000
LEN: WORD ?
POINTER: WORD 0x61E
START:
	CLA

LENGTH:
	IN 5
	AND #0x40
	BEQ LENGTH

	IN 4
	ST LEN
	ST (POINTER)+
FIRSTBYTE:
	IN 5
	AND #0x40
	BEQ FIRSTBYTE

	IN 4
	SWAB
	ST TEMP

	LD LEN
    DEC
    ST LEN
    BEQ STOP

SECONDBYTE:
	IN 5
	AND #0x40
	BEQ SECONDBYTE

	IN 4
	ADD TEMP
    ST (POINTER)+

    LD LEN
    DEC
    ST LEN
    BEQ STOP

    JUMP FIRSTBYTE

STOP:
    LD TEMP
    ST (POINTER)
    HLT